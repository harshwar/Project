/**
 * @description Connects to the blockchain, finds the first certificate
 * owned by the input address, and displays it.
 */
/**
 * @description Connects to the blockchain, finds the first certificate
 * owned by the input address, and displays it.
 */
async function viewCertificates() {
  const address = addressInput.value;

  // --- CHANGE #1: Updated for Ethers v6 ---
  if (!ethers.isAddress(address)) {
    statusMessage.value = "Please enter a valid Ethereum address.";
    return;
  }

  statusMessage.value = "Connecting to the blockchain...";

  try {
    // --- CHANGE #2: Updated for Ethers v6 ---
    const provider = new ethers.JsonRpcProvider(rpcUrl);
    const contract = new ethers.Contract(contractAddress, contractABI, provider);

    statusMessage.value = "Searching for certificate transfer events...";
    const filter = contract.filters.Transfer(null, address);

    const events = await contract.queryFilter(filter);

    if (events.length === 0) {
      statusMessage.value = "This address does not own any certificates.";
      nftImageUrl.value = ''; // Clear the image
      return;
    }

    statusMessage.value = `Found ${events.length} certificates. Loading first one...`;

    const firstEvent = events[0];
    const tokenId = firstEvent.args.tokenId;

    const tokenURI = await contract.tokenURI(tokenId);

    const metadataURL = tokenURI.replace("ipfs://", "https://ipfs.io/ipfs/");
    const response = await fetch(metadataURL);
    if (!response.ok) throw new Error("Could not fetch metadata from IPFS.");

    const metadata = await response.json();

    nftImageUrl.value = metadata.image.replace("ipfs://", "https://ipfs.io/ipfs/");
    statusMessage.value = `Displaying Certificate #${tokenId.toString()}`;

  } catch (error) {
    console.error("Error fetching certificates:", error);
    statusMessage.value = "Could not fetch certificates. See console for details.";
    nftImageUrl.value = ''; // Clear the image on error
  }
}
